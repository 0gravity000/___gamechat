<h2>【Fate／stay night DVD版 Windows10での実行方法】</h2>
<h3>1.まず、以下のサイトを確認します。</h3>
<a href="http://www.typemoon.com/faq/#fate26" target="_blank">http://www.typemoon.com/faq/#fate26</a><br>
<br>
<h3>2. \setup\setup.exe を実行しインストールします。</h3>
この時、以下の場所にインストール先を変更します。<br>
C:\Games\Fate／stay night<br>
※デフォルトの「C:\Program Files (x86)」だと、この後のディスクチェックに失敗します。<br>
<br>
<h3>3.このままでも、ディスクチェックに失敗しアプリが起動できないので、
以下のサイトの手順に従ってディスクチェックが成功するようにします。</h3>
<a href="https://www.typemoon.com/users/fate_disccheck/#disccheck" target="_blank">https://www.typemoon.com/users/fate_disccheck/#disccheck</a><br>
<br>
以下、手順の概要です。<br>
上記のサイトから、「protect_fate.zip」をダウンロードします。<br>
これを展開すると、「protect」フォルダがあります。<br>
C:\Games\Fate／stay night\protect フォルダごとコピーします。<br>
C:\Games\Fate／stay night\protect\mb_fate.exe があるはずなので、<br>
protect フォルダ内の mb_fate.exe を右クリックして「管理者として実行(A)」を選び、表示される説明に沿って処置を行う。<br>
説明に沿ってシリアルコードを入力したり、[ OK ]ボタンを押します。<br>
<br>
<h3>4. しかし、この操作でもディスクチェックに失敗します。</h3>
そこで以下のサイトの手順に従って操作します。<br>
Fate／stay night向けプロテクト誤動作サポートページ<br>
<a href="https://www.typemoon.com/users/casebycase/index2.php?for=FATE" target="_blank">https://www.typemoon.com/users/casebycase/index2.php?for=FATE</a><br>
・シリアルコード<br>
・送付先メールアドレス<br>
を記入し、「申込」ボタンを押します。<br>
<br>
<h3>5.送付先メールアドレスにメールが届き、「メールアドレス名.zip」 ファイルが添付されています。</h3>
「メールアドレス名.zip」ファイルを展開すると、「cxdec.tpm」 ファイルがあります。<br>
C:\Games\Fate／stay nightの<br>
「cxdec.tpm」<br>
を上書きコピーします。<br>
念のため、古いcxdec.tpmファイルをバックアップしておくことをおすすめします。<br>
ソフトを起動している場合は、一旦終了します。起動したままだと、「cxdec.tpm」を上書きできません。<br>
<br>
<h3>6.再度、ソフトを起動します。</h3>
ディスクチェックに成功し、メニュー画面が表示されます。<br>
<br>
※補足 セーブデータの保存先をユーザー別のドキュメントフォルダからインストール先フォルダに変更する必要があるかもしれません。<br>
C:\ユーザー\ご自分のユーザー名\Documents\fate_savedata<br>
　↓<br>
C:\Games\Fate／stay night\fate_savedata<br>
まるごとコピーが必要？<br>
<br>
