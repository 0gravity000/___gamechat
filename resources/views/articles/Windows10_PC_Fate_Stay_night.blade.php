<h2>【Fate／hollw ataraxia DVD版 Windows10での実行方法】</h2>
<h3>1. \setup\setup.exe を実行しインストールします。</h3>
この時、以下の場所にインストール先を変更します。<br>
C:\Games\Fate／hollow ataraxia<br>
※デフォルトの「C:\Program Files (x86)」だと、多分、この後のディスクチェックに失敗します。<br>
<br>
<h3>2.初回の起動時に、セーブデータの保存場所を聞かれます。</h3>
C:\Games\Fate／hollow ataraxia\fateha_savedata を指定します。<br>
<br>
<h3>3.しかし、ディスクチェック失敗します。</h3>
そこで以下のサイトの手順に従って操作します。<br>
TYPE-MOON ユーザーページ<br>
<a href="https://www.typemoon.com/users/" target="_blank">https://www.typemoon.com/users/</a><br>
このページの「シリアルコードでログイン」の箇所に、<br>
シリアルコードを入力し、「ログイン」ボタンを押します。<br>
<br>
<h3>4.ログインに成功すると以下のページが表示されます。</h3>
<a href="https://www.typemoon.com/users/download/" target="_bkank">https://www.typemoon.com/users/download/</a><br>
このページから、「protect_hollow.zip」をダウンロードし、展開します。<br>
<br>
<h3>5.「protect_hollow.zip」を展開すると「cxdec.tpm」ファイルと「protect」フォルダがあります。</h3>
インストール先の<br>
C:\Games\Fate／hollow ataraxia<br>
に、「cxdec.tpm」ファイルと「protect」コピーしてください。 <br>
<br>
<h3>6.C:\Games\Fate／hollow ataraxia\protectの中にある、
「mb_hollow.exe」<br>
を右クリックして管理者として実行します。</h3>
その後、シリアルコードを入力します。<br>
ディスクチェックに成功し、メニュー画面が表示されます。<br>
